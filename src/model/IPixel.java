package model;

/**
 * Represents an interface for Pixel.
 */
public interface IPixel {
  /**
   * Retrieves the Red value of the Pixel.
   * @return int red value of the pixel.
   */
  public int getRed();

  /**
   * Retrieves the Blue value of the Pixel.
   * @return int blue value of the pixel.
   */
  public int getBlue();

  /**
   * Retrieves the Green value of the Pixel.
   * @return int green value of the pixel.
   */
  public int getGreen();

  /**
   * Retrieves the Alpha value of the Pixel.
   * @return int alpha value of the pixel.
   */
  public int getAlpha();

}
